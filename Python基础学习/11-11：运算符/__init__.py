# 1）对两个数进行比较运算(自由选取一个比较运算符)
print(f"大于号对比：{2 > 1}")
print(f"小于号对比：{'b' < 'a'}")  # 字符的ascll码
print(f"等于号对比：{2 == 2}")
# 2）对两个字符串进行成员运算(用in 或者 not in)
a = 'abcdef'
print('成员变量in的作用', 'ab' in a, sep=':')  # 判断ab字符串是否存在与于a的元素里
print('成员变量not in的作用', 'x' in a, sep=':')  # 判断x字符串是否存在于a的元素里

