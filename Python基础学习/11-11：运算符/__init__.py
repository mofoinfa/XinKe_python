# #与或非
# 与：and, 两边条件为True 结果才是True 否则就是 False
# 或：or,只要一边满足一个True 就为True 否则就是 False
# 非：！,如为True 结果就是False   如果是False 结果为True
# 注意：print(true and 3)#此时返回的结果不为True而为3的bool的数据值但所表达的含义为True
# 类似print(false and None)#此时返回的结果为None但实际的含义为False，由此可得类似False含义的
# 还有’‘ ，false，None，0
